n1 = int(input('Digite o primeiro valor:'))
n2 = int(input('Digite o segundo valor:'))
soma = n1 + n2
subtracao = n1 - n2
multiplicacao = n1 * n2
divisao = n1 / n2
resto = n1 % n2
print('A soma é:{}'
      '\n a subtração é : {}'
      '\n a multiplicação é : {}'
      '\n a divisão é: {} '
      '\n o resto é: {}'.format(soma,subtracao,multiplicacao,divisao,resto))
